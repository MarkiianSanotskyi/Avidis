$(document).ready(function(){
   $('#header a[href*=#], #counter a[href*=#], #why-working a[href*=#], #price a[href*=#], #as-work a[href*=#], #counter-second a[href*=#], #reviews a[href*=#contact-anchor]').bind("click", function(e){
      var anchor = $(this);
   $(anchor).parent().parent().find('li').removeClass('current');
   $(anchor).parent('li').addClass('current');
   
      $('html, body').stop().animate({
         scrollTop: $(anchor.attr('href')).offset().top
      }, 800);
      e.preventDefault();
   });
   return false;
});

